package com.bluekjg.wxapp.model.wxApp20;

public class SkinDto {
    private String name;
    private String note;
    private WxSkinRecommend recommend;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public WxSkinRecommend getRecommend() {
        return recommend;
    }

    public void setRecommend(WxSkinRecommend recommend) {
        this.recommend = recommend;
    }
}
